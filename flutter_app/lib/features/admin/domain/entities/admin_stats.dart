import 'package:equatable/equatable.dart';

/// Represents revenue data for a specific day.
class RevenueByDay extends Equatable {
  /// The date in string format (e.g., '2023-10-01').
  final String date;

  /// Total revenue generated on this day.
  final double revenue;

  /// Number of bookings made on this day.
  final int bookingCount;

  const RevenueByDay({required this.date, required this.revenue, required this.bookingCount});

  @override
  List<Object?> get props => [date, revenue, bookingCount];
}

/// Represents booking statistics for a specific club.
class BookingsByClub extends Equatable {
