/// Represents session information for a specific club.
class ClubSessions extends Equatable {
  /// Unique identifier of the club.
  final int clubId;

  /// Name of the club.
  final String clubName;

  /// Total number of computers in the club.
  final int totalComputers;

  /// List of currently active sessions.
  final List<ClubSessionItem> activeSessions;

  /// List of upcoming sessions.
