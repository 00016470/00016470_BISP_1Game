/// Represents a club item for admin management.
class AdminClubItem extends Equatable {
  /// Unique identifier of the club.
  final int id;

  /// Name of the club.
  final String name;

  /// Location description of the club.
  final String location;

  /// Price per hour for computer usage.
  final int pricePerHour;

  /// Total number of computers available.
  final int totalComputers;

  /// Average rating of the club.
  final double rating;

  /// Whether the club is currently active.
  final bool isActive;

  /// Opening hour (24-hour format).
  final int openingHour;

  /// Closing hour (24-hour format).
  final int closingHour;

  /// Detailed address of the club (optional).
  final String? address;

  /// Latitude coordinate (optional).
  final double? latitude;

  /// Longitude coordinate (optional).
  final double? longitude;

  /// URL to the club's image (optional).
  final String? imageUrl;

  /// Total number of bookings for this club.
  final int bookingCount;

  /// Total revenue generated by this club.
  final double revenue;

  const AdminClubItem({
    required this.id, required this.name, required this.location,
    required this.pricePerHour, required this.totalComputers,
    required this.rating, required this.isActive,
    required this.openingHour, required this.closingHour,
    this.address, this.latitude, this.longitude, this.imageUrl,
    this.bookingCount = 0, this.revenue = 0,
  });

  @override
  List<Object?> get props => [id, name];
}

