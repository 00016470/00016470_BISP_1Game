/// Represents a booking item for admin management.
class AdminBookingItem extends Equatable {
  /// Unique identifier of the booking.
  final int id;

  /// ID of the user who made the booking.
  final int userId;

  /// Username of the user who made the booking.
  final String username;

  /// ID of the club where the booking is made.
  final int clubId;

  /// Name of the club.
  final String clubName;

  /// Start time of the booking.
  final DateTime startTime;

  /// Duration of the booking in hours.
  final double durationHours;

  /// Number of computers booked.
  final int computersBooked;

  /// Total price of the booking.
  final double totalPrice;

  /// Status of the booking (e.g., ACTIVE, COMPLETED).
  final String status;

  /// Payment method used (optional).
  final String? paymentMethod;

  /// Payment status (optional).
  final String? paymentStatus;

  /// Date and time when the booking was created.
  final DateTime createdAt;

  const AdminBookingItem({
    required this.id, required this.userId, required this.username,
    required this.clubId, required this.clubName,
    required this.startTime, required this.durationHours,
    required this.computersBooked, required this.totalPrice,
    required this.status, this.paymentMethod, this.paymentStatus,
    required this.createdAt,
  });

  /// Calculates the end time of the booking based on start time and duration.
  DateTime get endTime => startTime.add(Duration(minutes: (durationHours * 60).round()));

  @override
  List<Object?> get props => [id, status];
}

